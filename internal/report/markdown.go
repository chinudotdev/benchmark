// GenerateMarkdownReport creates a structured Markdown report from results
// in the given directory and writes it to report.md in that directory.
// Returns the path to the generated report.
func GenerateMarkdownReport(dir string) (string, error) {
	results, err := LoadResults(dir)
	if err != nil {
		return "", fmt.Errorf("load results: %w", err)
	}

	// Also check for sweep results in subdirectories
	sweepSummaries := discoverSweepResults(dir)

	// Load system info
	sysInfo, _ := loadSystemInfoFile(dir)

	var sb strings.Builder

	sb.WriteString("# GPU Benchmark Report\n\n")
	sb.WriteString(fmt.Sprintf("**Generated:** %s  \n", time.Now().Format(time.RFC3339)))
	sb.WriteString(fmt.Sprintf("**Results directory:** `%s`  \n\n", dir))

	// System info section
	writeSystemInfoSection(&sb, sysInfo)

	// Single-run results
	if len(results) > 0 {
		writeResultsSection(&sb, results)
	}

	// Sweep results
	for _, sw := range sweepSummaries {
		writeSweepSection(&sb, sw)
	}

	if len(results) == 0 && len(sweepSummaries) == 0 {
		sb.WriteString("*No results found.*\n\n")
	}

	sb.WriteString("---\n\n")
	sb.WriteString("*Generated by [gpu-benchmark](https://github.com/chinudotdev/gpu-benchmark)*\n")

	reportPath := filepath.Join(dir, "report.md")
	if err := os.WriteFile(reportPath, []byte(sb.String()), 0o644); err != nil {
		return "", fmt.Errorf("write report: %w", err)
	}

	return reportPath, nil
}

func writeSystemInfoSection(sb *strings.Builder, sysInfo *SystemInfo) {
	if sysInfo == nil {
		return
	}

	sb.WriteString("## System Configuration\n\n")
	sb.WriteString("| Component | Value |\n")
	sb.WriteString("|-----------|-------|\n")
	sb.WriteString(fmt.Sprintf("| Platform | %s |\n", sysInfo.Platform))
	sb.WriteString(fmt.Sprintf("| OS | %s |\n", sysInfo.OS))
	sb.WriteString(fmt.Sprintf("| Kernel | %s |\n", sysInfo.Kernel))
	sb.WriteString(fmt.Sprintf("| CPU | %s (%d cores) |\n", sysInfo.CPU.Model, sysInfo.CPU.Cores))
	sb.WriteString(fmt.Sprintf("| RAM | %d GB |\n", sysInfo.RAM_GB))
	sb.WriteString(fmt.Sprintf("| Docker | %s |\n", sysInfo.DockerVersion))
	sb.WriteString(fmt.Sprintf("| Docker Image | %s |\n", sysInfo.DockerImage))
	sb.WriteString(fmt.Sprintf("| Disk Available | %d GB |\n\n", sysInfo.DiskAvailGB))

	if len(sysInfo.Devices) > 0 {
		sb.WriteString("### Accelerators\n\n")
		sb.WriteString("| GPU | VRAM | Driver | Runtime |\n")
		sb.WriteString("|-----|------|--------|--------|\n")
		for _, dev := range sysInfo.Devices {
			sb.WriteString(fmt.Sprintf("| %s | %d GB | %s | %s |\n",
				dev.Name, dev.VRAM_GB, dev.DriverVersion, dev.RuntimeVersion))
		}
		sb.WriteString("\n")
	}
}

func writeResultsSection(sb *strings.Builder, results []*BenchmarkResult) {
	sb.WriteString("## Benchmark Results\n\n")

	// Summary table
	sb.WriteString("| Model | Quant | GPU | TPS | TTFT P99 | TPOT P99 | Cost/1M tok |\n")
	sb.WriteString("|-------|-------|-----|-----|----------|----------|-------------|\n")

	for _, r := range results {
		tps := "N/A"
		ttft := "N/A"
		tpot := "N/A"
		if r.Metrics != nil {
			tps = fmt.Sprintf("%.1f", r.Metrics.OutputTPS)
			if r.Metrics.TTFTP99 > 0 {
				ttft = r.Metrics.TTFTP99.Round(time.Microsecond).String()
			}
			if r.Metrics.TPOTP99 > 0 {
				tpot = r.Metrics.TPOTP99.Round(time.Microsecond).String()
			}
		}
		cost := "N/A"
		if r.Cost != nil && r.Cost.CostPer1MTokensUSD != nil {
			cost = fmt.Sprintf("$%.4f", *r.Cost.CostPer1MTokensUSD)
		}
		sb.WriteString(fmt.Sprintf("| %s | %s | %s | %s | %s | %s | %s |\n",
			r.ModelName, r.Quant, r.GPU, tps, ttft, tpot, cost))
	}
	sb.WriteString("\n")

	// Detailed per-model sections
	for _, r := range results {
		writeModelDetail(sb, r)
	}
}

func writeModelDetail(sb *strings.Builder, r *BenchmarkResult) {
	sb.WriteString(fmt.Sprintf("### %s\n\n", r.ModelName))
	if r.Metrics == nil {
		sb.WriteString("*No metrics available.*\n\n")
		return
	}
	m := r.Metrics

	sb.WriteString(fmt.Sprintf("- **Platform:** %s  \n", r.Platform))
	sb.WriteString(fmt.Sprintf("- **GPU:** %s × %d  \n", r.GPU, r.GPUCount))
	sb.WriteString(fmt.Sprintf("- **Benchmark config:** %d prompts, %d→%d tokens, c=%d, stream=%v  \n",
		r.Benchmark.NumPrompts, r.Benchmark.InputLen, r.Benchmark.OutputLen,
		r.Benchmark.Concurrency, r.Benchmark.Stream))
	sb.WriteString(fmt.Sprintf("- **Date:** %s  \n\n", r.Timestamp))

	sb.WriteString("#### Throughput\n\n")
	sb.WriteString("| Metric | Value |\n")
	sb.WriteString("|--------|-------|\n")
	sb.WriteString(fmt.Sprintf("| Output TPS | %.1f tok/s |\n", m.OutputTPS))
	sb.WriteString(fmt.Sprintf("| RPS | %.1f |\n", m.RPS))
	sb.WriteString(fmt.Sprintf("| t/s/user | %.2f |\n", m.TokensPerSecPerUser))
	sb.WriteString(fmt.Sprintf("| Total output tokens | %d |\n", m.TotalOutputTokens))
	sb.WriteString(fmt.Sprintf("| Success rate | %d/%d |\n", m.SuccessfulReqs, m.TotalRequests))
	if m.ColdStartS > 0 {
		sb.WriteString(fmt.Sprintf("| Cold start | %.1fs |\n", m.ColdStartS))
	}
	sb.WriteString("\n")

	if m.TTFTP50 > 0 {
		sb.WriteString("#### Latency (Streaming)\n\n")
		sb.WriteString("| Metric | P50 | P95 | P99 |\n")
		sb.WriteString("|--------|-----|-----|-----|\n")
		sb.WriteString(fmt.Sprintf("| E2E | %s | %s | %s |\n",
			m.LatencyP50.Round(time.Microsecond),
			m.LatencyP95.Round(time.Microsecond),
			m.LatencyP99.Round(time.Microsecond)))
		sb.WriteString(fmt.Sprintf("| TTFT | %s | %s | %s |\n",
			m.TTFTP50.Round(time.Microsecond),
			m.TTFTP95.Round(time.Microsecond),
			m.TTFTP99.Round(time.Microsecond)))
		sb.WriteString(fmt.Sprintf("| TPOT | %s | %s | %s |\n\n",
			m.TPOTP50.Round(time.Microsecond),
			m.TPOTP95.Round(time.Microsecond),
			m.TPOTP99.Round(time.Microsecond)))
	}

	if len(m.SLA) > 0 {
		sb.WriteString("#### SLA Compliance\n\n")
		sb.WriteString("| Band | Met | Total | Goodput (tok/s) |\n")
		sb.WriteString("|------|-----|-------|----------------|\n")
		for _, band := range []benchmark.SLABand{benchmark.SLABandInteractive, benchmark.SLABandConversational, benchmark.SLABandBatch} {
			if sla, ok := m.SLA[band]; ok {
				sb.WriteString(fmt.Sprintf("| %s | %d | %d | %.1f |\n",
					string(band), sla.MetCount, sla.TotalCount, sla.GoodputTPS))
			}
		}
		sb.WriteString("\n")
	}
}

func writeSweepSection(sb *strings.Builder, sw *mdSweepSummary) {
	sb.WriteString(fmt.Sprintf("## Sweep: %s\n\n", sw.ModelName))
	sb.WriteString(fmt.Sprintf("| Profile | Input | Output | Concurrency | TPS (mean) | TPS ±σ | TTFT P99 (ms) | TPOT P99 (ms) |\n"))
	sb.WriteString(fmt.Sprintf("|---------|-------|--------|-------------|------------|--------|---------------|---------------|\n"))
	for _, c := range sw.Cells {
		sb.WriteString(fmt.Sprintf("| %s | %d | %d | %d | %.1f | %.1f | %.1f | %.1f |\n",
			c.SeqProfile, c.InputLen, c.OutputLen, c.Concurrency,
			c.TPSMean, c.TPSStddev, c.TTFTP99MeanMs, c.TPOTP99MeanMs))
	}
	sb.WriteString("\n")
}

// --- sweep discovery and aggregation for markdown ---

type mdSweepSummary struct {
	ModelName string
	Cells     []mdSweepCell
}

type mdSweepCell struct {
	SeqProfile    string
	InputLen      int
	OutputLen     int
	Concurrency   int
	Repeats       int
	TPSMean       float64
	TPSStddev     float64
	TTFTP99MeanMs float64
	TPOTP99MeanMs float64
}

func discoverSweepResults(dir string) []*mdSweepSummary {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil
	}

	var summaries []*mdSweepSummary
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		subdir := filepath.Join(dir, entry.Name())
		cells, err := LoadSweepCells(subdir)
		if err != nil || len(cells) == 0 {
			continue
		}
		if sw := aggregateCellsForMD(cells); sw != nil {
			summaries = append(summaries, sw)
		}
	}
	return summaries
}

func aggregateCellsForMD(cells []*SweepCellResult) *mdSweepSummary {
	if len(cells) == 0 {
		return nil
	}

	sw := &mdSweepSummary{
		ModelName: cells[0].ModelName,
	}

	type key struct {
		profile             string
		inLen, outLen, conc int
	}
	type accum struct {
		count      int
		tpsSum     float64
		tpsSqSum   float64 // for stddev
		ttftSumMs  float64
		tpotSumMs  float64
		valid      int
	}
	groups := make(map[key]*accum)

	for _, c := range cells {
		k := key{c.SweepConfig.SeqProfile, c.SweepConfig.InputLen, c.SweepConfig.OutputLen, c.SweepConfig.Concurrency}
		a, ok := groups[k]
		if !ok {
			a = &accum{}
			groups[k] = a
		}
		a.count++

		// Parse metrics JSON
		var m struct {
			OutputTPS float64 `json:"output_tps"`
			TTFTP99   int64   `json:"ttft_p99"` // nanoseconds
			TPOTP99   int64   `json:"tpot_p99"`
		}
		if err := json.Unmarshal(c.Metrics, &m); err != nil || m.OutputTPS == 0 {
			continue
		}
		a.valid++
		a.tpsSum += m.OutputTPS
		a.tpsSqSum += m.OutputTPS * m.OutputTPS
		a.ttftSumMs += float64(m.TTFTP99) / 1e6
		a.tpotSumMs += float64(m.TPOTP99) / 1e6
	}

	for k, a := range groups {
		cell := mdSweepCell{
			SeqProfile:  k.profile,
			InputLen:    k.inLen,
			OutputLen:   k.outLen,
			Concurrency: k.conc,
			Repeats:     a.count,
		}
		if a.valid > 0 {
			cell.TPSMean = a.tpsSum / float64(a.valid)
			cell.TTFTP99MeanMs = a.ttftSumMs / float64(a.valid)
			cell.TPOTP99MeanMs = a.tpotSumMs / float64(a.valid)
			if a.valid > 1 {
				// Use sample variance (divide by n-1) to match AggregateSweep in sweep.go
				popVariance := (a.tpsSqSum / float64(a.valid)) - (cell.TPSMean * cell.TPSMean)
				if popVariance < 0 {
					popVariance = 0
				}
				sampleVariance := popVariance * float64(a.valid) / float64(a.valid-1)
				cell.TPSStddev = math.Sqrt(sampleVariance)
			}
		}
		sw.Cells = append(sw.Cells, cell)
	}

	return sw
}

func loadSystemInfoFile(dir string) (*SystemInfo, error) {
	path := filepath.Join(dir, "system_info.json")
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var si SystemInfo
	if err := json.Unmarshal(data, &si); err != nil {
		return nil, err
	}
	return &si, nil
}
